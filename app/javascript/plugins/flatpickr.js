import flatpickr from "flatpickr"
import "flatpickr/dist/flatpickr.min.css"

function initializeFlatpickr() {
  flatpickr(".datepicker", {
    minDate: "today",
    altInput: true,
    altFormat: "F j, Y",
    dateFormat: "Y-m-d",
  });

  flatpickr(".timepicker", {
    enableTime: true,
    noCalendar: true,
    dateFormat: "H:i",
  });
}

export default initializeFlatpickr;
